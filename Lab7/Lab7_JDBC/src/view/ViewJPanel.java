    private void submitEditedButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitEditedButtonActionPerformed
     // TODO add your handling code here:
     
     Patient newPatient = new Patient();
     newPatient.setFname(this.jTextFirstFieldName.getText());
     newPatient.setLname(this.jTextLastField.getText());
     newPatient.setAge(Integer.parseInt(this.jTextFieldAge.getText()));
     newPatient.setEmail(this.jTextFieldEmail.getText());
     newPatient.setMessage(this.jTextFieldMessage.getText());
     newPatient.setGender(this.buttonGroupGender.getSelection().getActionCommand());
     newPatient.setPatientType(this.patientTypejComboBox.getSelectedItem().toString());
     try{
     DatabaseConnector.updatePatient(selectedPatient,newPatient);
     }catch(Exception e){
         System.out.println("Update failed");
         System.out.println(e);
     }
    }//GEN-LAST:event_submitEditedButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = this.jTable.getSelectedRow();
        
        try{
            this.selectedPatient = this.patients.get(selectedRowIndex);
            DatabaseConnector.deletePatient(selectedPatient);
            System.out.println("Patient deleted succesfully ");
            populateTable();
        }catch (Exception e){
            System.out.println("Unable to delete the patient");
            System.out.println(e);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

