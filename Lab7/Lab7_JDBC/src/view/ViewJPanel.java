    public void populateTable(){
        this.patients = DatabaseConnector.getPatients();
        DefaultTableModel model = (DefaultTableModel) this.jTable.getModel();
        model.setRowCount(0);
        for (Patient p1: patients){
            Object [] row = new Object[8];
            
            row[0] = p1.getId();
            row[1] = p1.getFname();
            row[2] = p1.getLname();
            row[3] = p1.getAge();
            row[4] = p1.getEmail();
            row[5] = p1.getMessage();
            row[6] = p1.getGender();
            row[7] = p1.getPatientType();
            model.addRow(row);
            
        }
    }
    
    public void populateData(){
        
        this.jTextFirstFieldName.setText(selectedPatient.getFname());
        this.jTextLastField.setText(selectedPatient.getLname());
        //this.jTextFieldAge.setText(p1.getAge());
        this.jTextFieldAge.setText(String.valueOf(selectedPatient.getAge()));

        this.jTextFieldEmail.setText(selectedPatient.getEmail());
        this.jTextFieldMessage.setText(selectedPatient.getMessage());
        
        String gender = selectedPatient.getGender();
        
        if(gender.equals("MALE")){
            this.buttonGroupGender.setSelected(this.jRadioButtonMale.getModel(), true);
        } else if(gender.equals("FEMALE")){
            this.buttonGroupGender.setSelected(this.jRadioButtonFemale.getModel(), true);
        }else {
            this.buttonGroupGender.setSelected(this.jRadioButtonPreferNotToSay.getModel(), true);
        }
        
        this.patientTypejComboBox.setSelectedItem(selectedPatient.getPatientType());
        //this.imgJLabel.setIcon(p1.getProfilePicture());
        }
